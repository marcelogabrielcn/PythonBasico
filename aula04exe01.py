"""
Criar uma espécie de tela de cadastro simples. Com nome, idade, altura e verificar se é maior de idade.
"""

# Nome: String
print('Marcelo', type('Marcelo'))

# Idade: int
print(22, type(22))

# Altura: float
print(1.86, type(1.86))

# É maior?: bool
print(22 >= 18, type(22 >= 18))
