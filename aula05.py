"""
Operadores Aritméticos

+  Soma
-  Subtração
*  Multiplicação
/  Divisão
// Divisão inteiro
** Exponenciação
%  Resto da divisão
() Ordem de Precedência
"""

print(10 + 10)
print(10 - 10)
print(10 * 10)
print(10 / 10)
print(10 // 3)
print(10 ** 2)
print(10 % 3)

# O operador de multiplicação, também serve como repetição
print('K' * 30)

# O operador de soma, pode ser usado para fazer concatenação de Strings
print('Olá' + ' Mundo!')

# A ordem de precedência é igual na matemática, primeiro os parênteses, potenciação...
# ...multiplicação, divisão, soma e subtração
